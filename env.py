from entities import *

class Env(object):
    def __init__(self, stdscr):
        self.map = ["######################################",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "#....................................#",
                    "######################################"]
        self.scr = stdscr
        self.player = Player()

    def display(self):
        self.scr.erase()
        for line in self.map:
            self.scr.addstr(line + '\n')
        self.player.display(self.scr)
